package org.example.Ejercicio5;

public class Dupla {
    int cor_x;
    int cor_y;
    public Dupla(int x, int y) {
        this.cor_x = x;
        this.cor_y = y;
    }

    public int getCor_x() {
        return cor_x;
    }

    public void setCor_x(int cor_x) {
        this.cor_x = cor_x;
    }

    public int getCor_y() {
        return cor_y;
    }

    public void setCor_y(int cor_y) {
        this.cor_y = cor_y;
    }
}
